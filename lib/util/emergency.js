'use strict';
let log4js = require('log4js');
let logger = log4js.getLogger();
let util2 = require('./util2');

///////////////////////////////////////////////////////////////////////////////////////////////////
module.exports = {
    set: set,
    get: get
};

let EMERGENCY = null;

function set(value)
{
    logger.debug('emergency.js set value =' + util2.stringify2(value));
    EMERGENCY = value;
}
function get()
{
    logger.debug('emergency.js get value =' + util2.stringify2(EMERGENCY));
    return EMERGENCY;
}